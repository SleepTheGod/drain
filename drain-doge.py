def create_version_payload():
    version = 99999
    services = NODE_NETWORK
    timestamp = int(time.time())
    addr_recv = struct.pack('<Q16s', services, b'\x00' * 16)
    addr_from = struct.pack('<Q16s', services, b'\x00' * 16)
    nonce = 0
    user_agent = '1'.encode('utf-8')
    user_agent_length = len(user_agent)
    user_agent_length_compact = user_agent_length.to_bytes((user_agent_length.bit_length() + 7) // 8, 'little')
    start_height = 0
    relay = 1
    return struct.pack('<LQQ26s26sQ', version, services, timestamp, addr_recv, addr_from, nonce) + user_agent_length_compact + user_agent + struct.pack('<L?', start_height, relay)

def send_message(s, command, payload):
    magic = 0xC0C0C0C0
    command = command.encode('utf-8') + b'\x00' * (12 - len(command))
    length = len(payload)
    checksum = hashlib.sha256(hashlib.sha256(payload).digest()).digest()[:4]
    try:
        s.send(struct.pack('<L12sL4s', magic, command, length, checksum) + payload)
    except BrokenPipeError:
        time.sleep(0.00001)

def recv_message(s, thread_num):
    global total_mb_received
    magic, command, length, checksum = 0, b'', 0, b''
    try:
        magic, command, length, checksum = struct.unpack('<L12sL4s', s.recv(24))
    except struct.error:
        pass
    command = command.strip(b'\x00').decode('utf-8')
    payload = b''
    while len(payload) < length:
        chunk = s.recv(length - len(payload))
        if not chunk:
            break
        payload += chunk
    hex_payload = binascii.hexlify(payload[:50]).decode()
    total_mb_received += len(payload) / (1024 * 1024)
    print(f"[{thread_num}] {hex_payload} - {len(payload) / (1024 * 1024):.2f} MB - total: {total_mb_received:.2f} MB")
    return command, payload

