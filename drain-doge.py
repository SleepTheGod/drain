def process_headers(payload):
    headers_data = BytesIO(payload)
    num_headers = struct.unpack('<B', headers_data.read(1))[0]
    headers = []
    for _ in range(num_headers):
        header = headers_data.read(80)
        if len(header) < 80:
            break
        headers.append(header)
    return headers

