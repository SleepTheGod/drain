def send_message(s, command, payload):
    magic = 0xD9B4BEF9
    command = command.encode('utf-8') + b'\x00' * (12 - len(command))
    length = len(payload)
    checksum = hashlib.sha256(hashlib.sha256(payload).digest()).digest()[:4]
    message = struct.pack('<L12sL4s', magic, command, length, checksum) + payload
    s.sendall(message)

